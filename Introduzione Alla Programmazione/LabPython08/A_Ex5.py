def A_Ex5(a,b):
    insieme = set()
    j = 0
    lista_a = list(a)
    lista_b = list(b)
    for i in lista_a:
        x = lista_a[j]
        h = 0
        for i in lista_b:
            y = lista_b[h]
            if x[1] == y[0]:
                insieme.add((x[0],y[1]))
            h += 1
        j += 1
    return insieme









    

